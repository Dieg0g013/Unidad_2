
package eva2_9_for;
public class EVA2_9_FOR {
    public static void main(String[] args) {
        //3 Parametros para funciona
        //Punto inicial
        //Donde acaba
        //Como me voy a mover
        //i --> indice (arreglos)
        //++ Suma uno, -- restar uno
        for(int i = 1; i <= 10; i++){
            System.out.println(i + " Hola mundo!!");
            
        }
         for(int i = 10; i >= 1; i--){
            System.out.println(i + " Hola mundo!!");
        }
    }
    
}
